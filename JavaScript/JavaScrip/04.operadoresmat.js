//SUMA +
//RESTA -
//MULTIPLICACIÓN -> *
//DIVISIÓN -> /
//RESTO -> %

let x = 5;
let y = x + 3;
console.log(y);

let z = 10 * 4;
console.log(z);

let j = 10 / 2;
console.log(j);

//Suma y asignacion
let numA = 5;
//numA = numA + 10
numA += 10;
console.log(numA);
//Resta y asignacion
numA -= 5;
console.log(numA);
//Incremento
numA++;
console.log(numA);
//Decremento
numA--;
console.log(numA);
